     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => ['required'],
            'email' => ['required','unique:users'],
            'password' => ['required','min:5'],
        ]);

        $data = [
            'name' => $request->name,
            'email'=> $request->email,
            'password' =>  bcrypt($request->password)
        ];

        User::create($data);

